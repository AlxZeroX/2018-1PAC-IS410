str1 = 'Hola'
str2 = 'Mundo'

# Buscar una subcadena dentro de otra. Encuentra la posicion
str1.find("la")

# Encontrar si una cadena inicia con una subcadena especifica
str1.startswith("Ho")	# True

# Encontrar si una cadena termina con una subcadena especifica
str2.endswith('do')

